// Princípios importantes
// 1. Abstração = Perceber um objeto no mundo real e ter a capacidade de simplificar e traduzir ele para dentro do seu sistema
// 2. Encapsulamento = a capacidade de esconder os detalhes do código expondo apenas o necessário para o usuário
// 3. Herança (prototype)
// 4. Polimorfismo